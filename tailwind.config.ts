// This plugin adds each Tailwind color as a global CSS variable.
function addVariablesForColors({ addBase, theme }: PluginAPI) {
  const allColors: Record<string, string> = flattenColorPalette(theme("colors")); // Specify color types
  const newVars: Record<string, string> = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars,
  });
}

